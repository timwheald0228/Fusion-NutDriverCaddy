# Klein Nut Driver Caddy - Base Platform with Handle
# Proposed Revision 1.3
#
# This revision extends the proven Revision 1.2 base-plate architecture.
# It creates one manufacturable body with:
#   - 8.000 in overall width
#   - 9.500 in overall height
#   - 0.375 in thickness
#   - 0.500 in outside plan-view corner radius
#   - 4.000 x 1.000 in integrated rounded-rectangle handle opening
#   - 0.500 in top margin
#
# No mounting holes or shaft cradles are included.
#
# IMPORTANT WORKFLOW NOTE:
# Keep Revision 1.2 committed in Git before adopting this file. This proposed
# revision intentionally retains the existing body name so downstream work can
# continue to reference a stable generated body name.
#
# Fusion 360 API dimensions are internally stored in centimeters, but all
# controlling dimensions below are Fusion user parameters expressed in inches.

import adsk.core
import adsk.fusion
import traceback


SCRIPT_NAME = 'Klein Nut Driver Caddy - Base Platform Rev 1.3'
BODY_NAME = 'Klein Nut Driver Caddy Base Platform'

OUTER_SKETCH_NAME = 'Klein Caddy Base Platform Footprint'
HANDLE_SKETCH_NAME = 'Klein Caddy Handle Opening'


def get_or_create_parameter(design, name, expression, units, comment):
    """Create a Fusion user parameter, or update its expression and comment."""
    user_params = design.userParameters
    parameter = user_params.itemByName(name)

    if parameter:
        parameter.expression = expression
        parameter.comment = comment
        return parameter

    return user_params.add(
        name,
        adsk.core.ValueInput.createByString(expression),
        units,
        comment
    )


